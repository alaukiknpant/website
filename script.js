function setLanguage(lang) {
  if (!translations[lang]) return;

  document.querySelectorAll("[data-i18n]").forEach(function(el) {
    var key = el.getAttribute("data-i18n");
    if (translations[lang][key]) {
      el.innerHTML = translations[lang][key];
    }
  });

  // Update dropdown state
  document.querySelectorAll(".lang-option").forEach(function(opt) {
    opt.classList.toggle("active", opt.getAttribute("data-lang") === lang);
  });

  // Update the toggle label
  var currentLabel = document.querySelector(".lang-current");
  if (currentLabel) {
    currentLabel.textContent = langLabels[lang] || lang.toUpperCase();
  }

  document.documentElement.lang = lang;
  localStorage.setItem("lang", lang);
}

document.addEventListener("DOMContentLoaded", function() {
  // Lang dropdown toggle
  document.querySelectorAll(".lang-toggle").forEach(function(toggle) {
    toggle.addEventListener("click", function(e) {
      e.stopPropagation();
      var dropdown = this.closest(".lang-dropdown");
      dropdown.classList.toggle("open");
    });
  });

  // Language option click
  document.querySelectorAll(".lang-option").forEach(function(opt) {
    opt.addEventListener("click", function(e) {
      e.stopPropagation();
      setLanguage(this.getAttribute("data-lang"));
      this.closest(".lang-dropdown").classList.remove("open");
    });
  });

  // Close dropdown when clicking outside
  document.addEventListener("click", function() {
    document.querySelectorAll(".lang-dropdown.open").forEach(function(d) {
      d.classList.remove("open");
    });
  });

  // Restore saved language
  var saved = localStorage.getItem("lang");
  if (saved && translations[saved]) {
    setLanguage(saved);
  }
});
